package Components;


public class Navs {
    
    private boolean primeNav = false;
    private boolean defaultNav = false;
    private boolean bootstrapNav = true;

    public boolean isPrimeNav() {
        return primeNav;
    }

    public void setPrimeNav(boolean primeNav) {
        this.primeNav = primeNav;
    }

    public boolean isDefaultNav() {
        return defaultNav;
    }

    public void setDefaultNav(boolean defaultNav) {
        this.defaultNav = defaultNav;
    }

    public boolean isBootstrapNav() {
        return bootstrapNav;
    }

    public void setBootstrapNav(boolean bootstrapNav) {
        this.bootstrapNav = bootstrapNav;
    }

 

    
    

    
}
