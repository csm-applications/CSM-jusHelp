package Components;


public class Footers {
    private boolean defaultFooter = true   ;
    private boolean slimFooter = false;

    public boolean isDefaultFooter() {
        return defaultFooter;
    }

    public void setDefaultFooter(boolean defaultFooter) {
        this.defaultFooter = defaultFooter;
    }

    public boolean isSlimFooter() {
        return slimFooter;
    }

    public void setSlimFooter(boolean slimFooter) {
        this.slimFooter = slimFooter;
    }
    
    
}
