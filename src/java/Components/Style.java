package Components;

public class Style {
    
    private boolean defaultStyle = false;
    private boolean metalStyle = false;
    private boolean greenStyle = true;

    public boolean isDefaultStyle() {
        return defaultStyle;
    }

    public void setDefaultStyle(boolean defaultStyle) {
        this.defaultStyle = defaultStyle;
    }

    public boolean isMetalStyle() {
        return metalStyle;
    }

    public void setMetalStyle(boolean metalStyle) {
        this.metalStyle = metalStyle;
    }

    public boolean isGreenStyle() {
        return greenStyle;
    }

    public void setGreenStyle(boolean greenStyle) {
        this.greenStyle = greenStyle;
    }

    
    
    
}
